package main

import (
	"fmt"
	"strings"
)

func main() {
	rows := strings.Split(input, "\n")
	width := len(rows[0])
	height := len(rows)
	fmt.Println(input)
	fmt.Println("")

	var grid = make([][]string, width)
	for i := range grid {
		grid[i] = make([]string, height)
	}

	straightInput := strings.ReplaceAll(input, "\n", "")
	for i, c := range straightInput {
		char := string(c)
		y := i / height
		x := i % width

		grid[x][y] = char
	}

	ans := 0
	for j := 0; j < height; j++ {
		for i := 0; i < width; i++ {
			count := findXMAS(grid, i, j)
			ans += count
			if count > 0 {
				fmt.Print(count)
			} else {
				fmt.Print("_")
			}
		}
		fmt.Print("\n")
	}
	fmt.Println(ans)
}

func query(grid [][]string, x int, y int, match string) bool {
	if x < 0 || x >= len(grid[0]) {
		return false
	}
	if y < 0 || y >= len(grid) {
		return false
	}

	return grid[x][y] == match
}

func findXMAS(grid [][]string, x int, y int) int {
	count := 0

	if findWord(grid, "XMAS", x, y, -1, -1) {
		count++
	}
	if findWord(grid, "XMAS", x, y, 0, -1) {
		count++
	}
	if findWord(grid, "XMAS", x, y, 1, -1) {
		count++
	}
	if findWord(grid, "XMAS", x, y, -1, 0) {
		count++
	}
	if findWord(grid, "XMAS", x, y, 1, 0) {
		count++
	}
	if findWord(grid, "XMAS", x, y, -1, 1) {
		count++
	}
	if findWord(grid, "XMAS", x, y, 0, 1) {
		count++
	}
	if findWord(grid, "XMAS", x, y, 1, 1) {
		count++
	}

	return count
}

func findWord(grid [][]string, word string, x int, y int, dx int, dy int) bool {
	for i, l := range word {
		letter := string(l)
		if !query(grid, x+i*dx, y+i*dy, letter) {
			return false
		}
	}
	return true
}

