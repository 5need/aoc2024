package main

import (
	"fmt"
	"strings"
)

func main() {
	rows := strings.Split(input, "\n")
	width := len(rows[0])
	height := len(rows)

	var grid = make([][]string, width)
	for i := range grid {
		grid[i] = make([]string, height)
	}

	straightInput := strings.ReplaceAll(input, "\n", "")
	for i, c := range straightInput {
		char := string(c)
		y := i / height
		x := i % width

		grid[x][y] = char
	}

	ans := 0
	for j := 0; j < height; j++ {
		for i := 0; i < width; i++ {
			currentChar := query(grid, i, j)

			c := findXMAS(grid, i, j)
			if c > 0 {
				fmt.Print("_")
				ans++
			} else {
				fmt.Print(currentChar)
			}
		}
		fmt.Print("\n")
	}
	fmt.Println(ans)
}

func query(grid [][]string, x int, y int) string {
	if x < 0 || x >= len(grid) {
		return ""
	}
	if y < 0 || y >= len(grid[0]) {
		return ""
	}

	return grid[x][y]
}

func findXMAS(grid [][]string, x int, y int) int {
	currentChar := grid[x][y]

	if currentChar == "A" {
		// just add up the tl tr bl br M's and S's
		mCount := 0
		sCount := 0

		tl := query(grid, x-1, y-1)
		if tl == "M" {
			mCount++
		} else if tl == "S" {
			sCount++
		} else {
			return 0
		}

		tr := query(grid, x+1, y-1)
		if tr == "M" {
			mCount++
		} else if tr == "S" {
			sCount++
		} else {
			return 0
		}

		bl := query(grid, x-1, y+1)
		if bl == "M" {
			mCount++
		} else if bl == "S" {
			sCount++
		} else {
			return 0
		}

		br := query(grid, x+1, y+1)
		if br == "M" {
			mCount++
		} else if br == "S" {
			sCount++
		} else {
			return 0
		}

		if mCount == 2 && sCount == 2 && tl != br && tr != bl {
			return 1
		} else {
			return 0
		}
	}
	return 0
}

